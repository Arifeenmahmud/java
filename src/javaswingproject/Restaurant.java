    private void btnConvartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConvartActionPerformed
        // TODO add your handling code here:
        try {
            double Bangladeshi = 89.07;
            double Indian = 77.64;
            double Pakistani = 200.10;
            double USD = Double.parseDouble(txtEnterCurrency.getText());

            if (Selectcountry.getSelectedItem().equals("Bangladesh")) {
                String bdt = String.format("BDT %.2f", USD * Bangladeshi);
                txtEnterCurrencyOutput.setText(bdt);
            }
            if (Selectcountry.getSelectedItem().equals("India")) {
                String Indiarupi = String.format("Rupi %.2f", USD * Indian);
                txtEnterCurrencyOutput.setText(Indiarupi);

            }
            if (Selectcountry.getSelectedItem().equals("Pakestan")) {
                String Pakestanirupi = String.format("Rupi %.2f", USD * Pakistani);
                txtEnterCurrencyOutput.setText(Pakestanirupi);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "please enter a ammunt");
        }


    }//GEN-LAST:event_btnConvartActionPerformed

