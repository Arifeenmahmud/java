    public void times() {
        Thread dateTime = new Thread() {
            public void run() {

                try {
                    for (;;) {
                        Calendar cal = new GregorianCalendar();

                        int second = cal.get(Calendar.SECOND);
                        int minute = cal.get(Calendar.MINUTE);
                        int hours = cal.get(Calendar.HOUR);

                        time.setText("Time: " + hours + ":" + minute + ":" + second);
                        sleep(1000);

                    }

                } catch (Exception e) {
                }

            }
        };
        dateTime.start();

    }

