    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:

        try {
            //=================Drink==============

            double drinks = Double.parseDouble(TxtChooseDrink.getText());
            double CocaCola = drinks * 40;
            double Pepsi = drinks * 45;

            if (btnChooseDrink.getSelectedItem().equals("Coca Cola")) {

                String Coca_Cola = String.format("%.2f", CocaCola);
                txtTotalCostOfDrink.setText(Coca_Cola);
            }
            if (btnChooseDrink.getSelectedItem().equals("Pepsi")) {

                String Pepsi_Cola = String.format("%.2f", Pepsi);
                txtTotalCostOfDrink.setText(Pepsi_Cola);
            }
            if (btnChooseDrink.getSelectedItem().equals("Choose Drinks")) {

                txtTotalCostOfDrink.setText("00");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please set a QTY and Drinks");
        }

        //================meal==================
        try {
            double FriedRice = Double.parseDouble(TxtFriedRice.getText());
            double PFriedRice = FriedRice * 240;
            String SPFriedRice = String.format("%.2f", PFriedRice);
            txtTotalCostOfMeal.setText(SPFriedRice);

            double Shawarma = Double.parseDouble(TxtShawarma.getText());
            double pShawarma = Shawarma * 85;
            String spShawarma = String.format("%.2f", pShawarma + PFriedRice);
            txtTotalCostOfMeal.setText(spShawarma);

            double Chicken = Double.parseDouble(TxtChicken.getText());
            double pChicken = Chicken * 60;
            String spChicken = String.format("%.2f", pShawarma + PFriedRice + pChicken);
            txtTotalCostOfMeal.setText(spChicken);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }

        //===========Delivery===========
        double cDelivery = 18;
        if (btnDelivery.isSelected()) {
            String pDelivery = String.format("%.2f", cDelivery);
            txtCostOfDelivery.setText(pDelivery);
        } else {
            txtCostOfDelivery.setText("00");
        }

        //===========tax===========
        double txtTotalCostOfMeal = Double.parseDouble(this.txtTotalCostOfMeal.getText());
        double txtTotalCostOfDrink = Double.parseDouble(this.txtTotalCostOfDrink.getText());
        double txtCostOfDelivery = Double.parseDouble(this.txtCostOfDelivery.getText());
        double tax15 = ((txtTotalCostOfMeal + txtTotalCostOfDrink + txtCostOfDelivery) / 100) * 15;
        if (btnTax.isSelected()) {

            String tx = String.format("%.2f", tax15);
            txtTax.setText(tx);
        }

        //==============Sub Total=================
        double subtotal = txtTotalCostOfMeal + txtTotalCostOfDrink + txtCostOfDelivery;
        String Ssubtotal = String.format("%.2f", subtotal);

        txtSubTotal.setText(Ssubtotal);

        //============Total============
        double total = tax15 + subtotal;
        String stotal = String.format("%.2f", total);

        txtTotal.setText(stotal);
        
        
        //==========Recipt===========
        Double Invoice1 = Double.parseDouble(this.txtTotalCostOfMeal.getText());
        Double Invoice2 = Double.parseDouble(this.txtTotalCostOfDrink.getText());
        Double Invoice3 = Double.parseDouble(this.txtCostOfDelivery.getText());
        Double Invoice4 = Double.parseDouble(this.txtTax.getText());
        Double Invoice5 = Double.parseDouble(this.txtSubTotal.getText());
        Double Invoice6 = Double.parseDouble(this.txtTotal.getText());
        
        jTextArea1.append("\n Total Cost Info:\n"
                +"\n Total Cost Of Meal: \t" +Invoice1
                +"\n Total Cost Of Drink:\t" +Invoice2
                +"\n Cost Of Delivery:\t" +Invoice4
                +"\n Tax:\t\t" +Invoice5
                +"\n Sub Total:\t\t" +Invoice6
                +"\n Total:\t\t" +Invoice6
        
        
        );

    }//GEN-LAST:event_btnSubmitActionPerformed

